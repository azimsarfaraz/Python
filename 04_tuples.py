# creating a tuple using()
t = (1,2,4,5,1,6,7,1)
t1= () #empty tuple
t2= (6,) #tuple with single element
print(t[0]) #printing elements of tuples
#t[0] = 34 #cannot change elements from tuples
print(t1)
print(t2)

#tuple methods - for more python.org
print(t.count(1)) #count no of occurrences of value 1
print(t.index(7)) # finds the index value of the value 7

